fn basename_of(relative_path: &str) -> &str {
    relative_path
        .rsplit_once('/')
        .map(|(_, name)| name)
        .unwrap_or(relative_path)
}

fn directory_of(relative_path: &str) -> &str {
    relative_path
        .rsplit_once('/')
        .map(|(dir, _)| dir)
        .unwrap_or("")
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn cargo_lockfile_points_to_manifest_sibling() {
        let hint = detect_lineage("Cargo.lock").expect("Cargo.lock must produce a hint");
        assert_eq!(hint.generated_class, GeneratedArtifactClass::Lockfile);
        assert_eq!(
            hint.source_canonical_relative_path.as_deref(),
            Some("Cargo.toml")
        );
        assert_eq!(hint.rule_id, "lockfile.cargo");
        assert_eq!(
            hint.freshness_class,
            LineageFreshnessClass::DerivedFromCanonical
        );
    }

    #[test]
    fn nested_cargo_lockfile_resolves_relative_to_directory() {
        let hint = detect_lineage("crates/aureline-shell/Cargo.lock")
            .expect("nested Cargo.lock must produce a hint");
        assert_eq!(
            hint.source_canonical_relative_path.as_deref(),
            Some("crates/aureline-shell/Cargo.toml")
        );
    }

    #[test]
    fn target_subtree_is_build_output_without_canonical_sibling() {
        let hint = detect_lineage("target/debug/build/foo.rs")
            .expect("target subtree must produce a hint");
        assert_eq!(hint.generated_class, GeneratedArtifactClass::BuildOutput);
        assert!(hint.source_canonical_relative_path.is_none());
        assert_eq!(
            hint.freshness_class,
            LineageFreshnessClass::PossiblyStale
        );
    }

    #[test]
    fn gen_rs_strips_suffix_for_canonical_sibling() {
        let hint = detect_lineage("crates/aureline-rpc/src/api.gen.rs")
            .expect(".gen.rs must produce a hint");
        assert_eq!(
            hint.generated_class,
            GeneratedArtifactClass::GeneratedSourceSibling
        );
        assert_eq!(
            hint.source_canonical_relative_path.as_deref(),
            Some("crates/aureline-rpc/src/api.rs")
        );
    }

    #[test]
    fn vendored_node_modules_is_snapshot_only() {
        let hint = detect_lineage("node_modules/lodash/index.js")
            .expect("node_modules subtree must produce a hint");
        assert_eq!(
            hint.generated_class,
            GeneratedArtifactClass::VendoredSnapshot
        );
        assert_eq!(hint.freshness_class, LineageFreshnessClass::SnapshotOnly);
        assert!(hint.source_canonical_relative_path.is_none());
    }

    #[test]
    fn ordinary_source_file_returns_no_hint() {
        assert!(detect_lineage("src/main.rs").is_none());
        assert!(detect_lineage("README.md").is_none());
        assert!(detect_lineage("docs/notes.md").is_none());
    }

    #[test]
    fn unknown_path_with_lockfile_basename_in_subdir_still_matches() {
        let hint = detect_lineage("services/api/package-lock.json")
            .expect("nested package-lock.json must match");
        assert_eq!(
            hint.source_canonical_relative_path.as_deref(),
            Some("services/api/package.json")
        );
        assert_eq!(hint.rule_id, "lockfile.npm");
    }

    #[test]
    fn record_round_trips_through_serde() {
        let hint = detect_lineage("Cargo.lock").expect("hint must exist");
        let json = serde_json::to_string(&hint).expect("serialize");
        let parsed: LineageHintRecord = serde_json::from_str(&json).expect("parse");
        assert_eq!(parsed, hint);
    }

    #[test]
    fn empty_catalog_returns_none() {
        let catalog = GeneratedArtifactCatalog::new();
        assert!(catalog.detect("Cargo.lock").is_none());
    }

    #[test]
    fn directory_prefix_does_not_match_partial_segment() {
        // The "target" directory prefix must not greedily match a path that
        // merely starts with the literal characters "target" (e.g. a folder
        // named `targets/`).
        let hint = detect_lineage("targets/manifest.json");
        assert!(hint.is_none());
    }

    #[test]
    fn first_matching_rule_wins() {
        // Build a synthetic catalog where two rules could match the same
        // basename and confirm the first wins.
        let catalog = GeneratedArtifactCatalog::with_rules(vec![
            GeneratedArtifactRule {
                rule_id: "first.rule".to_string(),
                class: GeneratedArtifactClass::Lockfile,
                matcher: RuleMatcher::BasenameEquals {
                    basename: "test.lock".to_string(),
                },
                source_canonical: SourceCanonicalLink::None,
                producer_id: "producer:first".to_string(),
                producer_label: "First".to_string(),
                freshness_class: LineageFreshnessClass::Unknown,
                explainer: "first".to_string(),
            },
            GeneratedArtifactRule {
                rule_id: "second.rule".to_string(),
                class: GeneratedArtifactClass::Lockfile,
                matcher: RuleMatcher::BasenameSuffix {
                    suffix: ".lock".to_string(),
                },
                source_canonical: SourceCanonicalLink::None,
                producer_id: "producer:second".to_string(),
                producer_label: "Second".to_string(),
                freshness_class: LineageFreshnessClass::Unknown,
                explainer: "second".to_string(),
            },
        ]);
        let hint = catalog.detect("test.lock").expect("matches first rule");
        assert_eq!(hint.rule_id, "first.rule");
    }
}
