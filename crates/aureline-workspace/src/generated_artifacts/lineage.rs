//! Generated-artifact lineage records, rules, and detector.
//!
//! Every projection here works on workspace-relative, forward-slash-normalized
//! paths. Callers that hold absolute paths or platform paths must translate
//! into the workspace-relative form before consulting the detector — the
//! lineage truth never inspects the filesystem.

use serde::{Deserialize, Serialize};

/// Schema version for the seeded lineage-hint payload shape.
pub type LineageHintSchemaVersion = u32;

/// Identifies the `generated_artifact_lineage_record` record kind.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum LineageHintRecordKind {
    /// `generated_artifact_lineage_record`
    GeneratedArtifactLineageRecord,
}

/// Closed vocabulary for the kind of generated artifact a row represents.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum GeneratedArtifactClass {
    /// Lockfile (e.g., `Cargo.lock`, `package-lock.json`).
    Lockfile,
    /// Build output produced by a compiler / bundler / packager.
    BuildOutput,
    /// Generated source file co-located with hand-authored code (e.g.,
    /// `*.gen.rs`, `*_pb.rs`).
    GeneratedSourceSibling,
    /// Snapshot of third-party source vendored into the workspace.
    VendoredSnapshot,
}

impl GeneratedArtifactClass {
    /// Stable token used in records, fixtures, and shell snapshots.
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Lockfile => "lockfile",
            Self::BuildOutput => "build_output",
            Self::GeneratedSourceSibling => "generated_source_sibling",
            Self::VendoredSnapshot => "vendored_snapshot",
        }
    }

    /// Short attribution badge surfaces render next to a row to disclose the
    /// generated-versus-canonical boundary.
    pub const fn badge(self) -> &'static str {
        match self {
            Self::Lockfile => "lockfile",
            Self::BuildOutput => "build output",
            Self::GeneratedSourceSibling => "generated",
            Self::VendoredSnapshot => "vendored",
        }
    }

    /// Human-readable label suitable for an explorer/search row hint.
    pub const fn label(self) -> &'static str {
        match self {
            Self::Lockfile => "Lockfile (resolver-generated; edit the manifest)",
            Self::BuildOutput => "Build output (regenerated by the producer)",
            Self::GeneratedSourceSibling => "Generated source (edit the canonical sibling)",
            Self::VendoredSnapshot => "Vendored snapshot (third-party copy; edit upstream)",
        }
    }
}

/// Closed vocabulary describing how trustworthy the lineage inference is.
///
/// Surfaces SHOULD render the freshness label directly; they MUST NOT collapse
/// `possibly_stale` into a generic "loading" badge or hide `unknown` behind a
/// silent fallback.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum LineageFreshnessClass {
    /// Lineage was derived from a co-located canonical sibling we just
    /// inspected; the generated row is expected to be in sync.
    DerivedFromCanonical,
    /// Lineage is known but freshness against the canonical source cannot be
    /// confirmed from path inspection alone.
    PossiblyStale,
    /// Vendored / mirrored snapshot. The local copy is intentionally not
    /// regenerated; surfaces should pivot to the upstream when relevant.
    SnapshotOnly,
    /// Lineage class is known but the freshness signal is unavailable.
    Unknown,
}

impl LineageFreshnessClass {
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::DerivedFromCanonical => "derived_from_canonical",
            Self::PossiblyStale => "possibly_stale",
            Self::SnapshotOnly => "snapshot_only",
            Self::Unknown => "unknown",
        }
    }

    /// Short label suitable for a chrome chip.
    pub const fn label(self) -> &'static str {
        match self {
            Self::DerivedFromCanonical => "Derived",
            Self::PossiblyStale => "Possibly stale",
            Self::SnapshotOnly => "Snapshot",
            Self::Unknown => "Unknown",
        }
    }
}

/// Lineage hint emitted for one workspace-relative path.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct LineageHintRecord {
    pub record_kind: LineageHintRecordKind,
    pub schema_version: LineageHintSchemaVersion,
    /// Workspace-relative path (forward-slash) of the generated artifact.
    pub generated_relative_path: String,
    /// Generated-artifact class.
    pub generated_class: GeneratedArtifactClass,
    /// Workspace-relative path (forward-slash) of the source-canonical
    /// artifact, when known. `None` for build outputs that have no single
    /// canonical sibling and for vendored snapshots whose canonical source is
    /// upstream.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub source_canonical_relative_path: Option<String>,
    /// Stable producer identifier (e.g., `producer:cargo_lockfile`).
    pub producer_id: String,
    /// Human-readable producer label for chrome.
    pub producer_label: String,
    /// Lineage freshness class.
    pub freshness_class: LineageFreshnessClass,
    /// Short explainer suitable for tooltips and a11y exports.
    pub explainer: String,
    /// Stable rule identifier that produced the hint.
    pub rule_id: String,
}

impl LineageHintRecord {
    /// Stable record-kind tag carried in serialized records.
    pub const RECORD_KIND: &'static str = "generated_artifact_lineage_record";

    /// Schema version for the seeded payload shape.
    pub const SCHEMA_VERSION: LineageHintSchemaVersion = 1;

    /// True when the hint identifies a source-canonical sibling the user can
    /// pivot to.
    pub fn has_source_canonical(&self) -> bool {
        self.source_canonical_relative_path.is_some()
    }
}

/// Rule matcher vocabulary.
///
/// Matchers operate on workspace-relative, forward-slash-normalized paths.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum RuleMatcher {
    /// Match when the basename equals the given string (case-sensitive).
    BasenameEquals { basename: String },
    /// Match when the basename ends with the given suffix (case-sensitive).
    BasenameSuffix { suffix: String },
    /// Match when the path begins with the given directory prefix
    /// (e.g. `target/`). The prefix must end with `/`.
    DirectoryPrefix { prefix: String },
}

impl RuleMatcher {
    fn matches(&self, relative_path: &str) -> bool {
        let basename = basename_of(relative_path);
        match self {
            RuleMatcher::BasenameEquals { basename: needle } => basename == needle.as_str(),
            RuleMatcher::BasenameSuffix { suffix } => basename.ends_with(suffix.as_str()),
            RuleMatcher::DirectoryPrefix { prefix } => relative_path.starts_with(prefix.as_str()),
        }
    }
}

/// How to derive the source-canonical path from the matched generated path.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum SourceCanonicalLink {
    /// No source-canonical sibling. Used for build outputs whose canonical
    /// source spans many files.
    None,
    /// Source canonical lives in the same directory as the generated artifact
    /// under the named basename (e.g. `Cargo.lock` → `Cargo.toml`).
    SiblingByName { basename: String },
    /// Source canonical is the same path with the given suffix stripped from
    /// the basename and replaced with the named replacement (e.g.
    /// `*.gen.rs` → `*.rs`).
    StripBasenameSuffix {
        suffix: String,
        replacement: String,
    },
}

impl SourceCanonicalLink {
    fn resolve(&self, relative_path: &str) -> Option<String> {
        match self {
            SourceCanonicalLink::None => None,
            SourceCanonicalLink::SiblingByName { basename } => {
                let dir = directory_of(relative_path);
                if dir.is_empty() {
                    Some(basename.clone())
                } else {
                    Some(format!("{dir}/{basename}"))
                }
            }
            SourceCanonicalLink::StripBasenameSuffix {
                suffix,
                replacement,
            } => {
                let original_basename = basename_of(relative_path);
                let stripped = original_basename.strip_suffix(suffix.as_str())?;
                let new_basename = format!("{stripped}{replacement}");
                let dir = directory_of(relative_path);
                if dir.is_empty() {
                    Some(new_basename)
                } else {
                    Some(format!("{dir}/{new_basename}"))
                }
            }
        }
    }
}

/// One generated-artifact rule.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct GeneratedArtifactRule {
    pub rule_id: String,
    pub class: GeneratedArtifactClass,
    pub matcher: RuleMatcher,
    pub source_canonical: SourceCanonicalLink,
    pub producer_id: String,
    pub producer_label: String,
    pub freshness_class: LineageFreshnessClass,
    pub explainer: String,
}

impl GeneratedArtifactRule {
    fn apply(&self, relative_path: &str) -> Option<LineageHintRecord> {
        if !self.matcher.matches(relative_path) {
            return None;
        }
        Some(LineageHintRecord {
            record_kind: LineageHintRecordKind::GeneratedArtifactLineageRecord,
            schema_version: LineageHintRecord::SCHEMA_VERSION,
            generated_relative_path: relative_path.to_string(),
            generated_class: self.class,
            source_canonical_relative_path: self.source_canonical.resolve(relative_path),
            producer_id: self.producer_id.clone(),
            producer_label: self.producer_label.clone(),
            freshness_class: self.freshness_class,
            explainer: self.explainer.clone(),
            rule_id: self.rule_id.clone(),
        })
    }
}

/// Ordered set of generated-artifact rules. The first rule that matches wins.
#[derive(Debug, Clone, Default, PartialEq, Eq, Serialize, Deserialize)]
pub struct GeneratedArtifactCatalog {
    rules: Vec<GeneratedArtifactRule>,
}

impl GeneratedArtifactCatalog {
    /// Empty catalog (no rules).
    pub fn new() -> Self {
        Self::default()
    }

    /// Build a catalog from an explicit rule list. The list is consumed and
    /// the first matching rule wins on detection.
    pub fn with_rules(rules: Vec<GeneratedArtifactRule>) -> Self {
        Self { rules }
    }

    /// Borrow the rule list.
    pub fn rules(&self) -> &[GeneratedArtifactRule] {
        &self.rules
    }

    /// Append an additional rule to the catalog. Useful for tests / fixtures
    /// that want to extend the default rule set without reconstructing it.
    pub fn push(&mut self, rule: GeneratedArtifactRule) {
        self.rules.push(rule);
    }

    /// Detect lineage for the given workspace-relative path. Returns `None`
    /// when no rule matches.
    pub fn detect(&self, relative_path: &str) -> Option<LineageHintRecord> {
        for rule in &self.rules {
            if let Some(hint) = rule.apply(relative_path) {
                return Some(hint);
            }
        }
        None
    }
}

/// Detect lineage against the default catalog. Convenience wrapper around
/// [`GeneratedArtifactCatalog::detect`].
pub fn detect_lineage(relative_path: &str) -> Option<LineageHintRecord> {
    default_catalog().detect(relative_path)
}

