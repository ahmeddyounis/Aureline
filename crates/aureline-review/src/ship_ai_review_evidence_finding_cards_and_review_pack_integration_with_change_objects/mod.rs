impl EvidenceCardPacket {
    /// Builds an AI review-evidence finding-card packet from stable-lane input.
    pub fn new(input: EvidenceCardPacketInput) -> Self {
        Self {
            record_kind: EVIDENCE_CARD_RECORD_KIND.to_owned(),
            schema_version: EVIDENCE_CARD_SCHEMA_VERSION,
            packet_id: input.packet_id,
            surface_label: input.surface_label,
            evidence_rows: input.evidence_rows,
            finding_cards: input.finding_cards,
            review_pack_bindings: input.review_pack_bindings,
            downgrade_triggers: input.downgrade_triggers,
            consumer_surfaces: input.consumer_surfaces,
            trust_review: input.trust_review,
            consumer_projection: input.consumer_projection,
            proof_freshness: input.proof_freshness,
            source_contract_refs: input.source_contract_refs,
            redaction_class_token: input.redaction_class_token,
            minted_at: input.minted_at,
        }
    }

    /// Validates the AI review-evidence finding-card invariants.
    pub fn validate(&self) -> Vec<EvidenceCardViolation> {
        let mut violations = Vec::new();

        if self.record_kind != EVIDENCE_CARD_RECORD_KIND {
            violations.push(EvidenceCardViolation::WrongRecordKind);
        }
        if self.schema_version != EVIDENCE_CARD_SCHEMA_VERSION {
            violations.push(EvidenceCardViolation::WrongSchemaVersion);
        }
        if self.packet_id.trim().is_empty()
            || self.surface_label.trim().is_empty()
            || self.redaction_class_token.trim().is_empty()
            || self.minted_at.trim().is_empty()
        {
            violations.push(EvidenceCardViolation::MissingIdentity);
        }
        if self.downgrade_triggers.is_empty() {
            violations.push(EvidenceCardViolation::DowngradeTriggersMissing);
        }
        if self.consumer_surfaces.is_empty() {
            violations.push(EvidenceCardViolation::ConsumerSurfacesMissing);
        }

        validate_source_contracts(self, &mut violations);
        validate_evidence_rows(self, &mut violations);
        validate_bindings(self, &mut violations);
        validate_finding_cards(self, &mut violations);

        if !self.trust_review.all_hold() {
            violations.push(EvidenceCardViolation::TrustReviewIncomplete);
        }
        if !self.consumer_projection.all_hold() {
            violations.push(EvidenceCardViolation::ConsumerProjectionIncomplete);
        }
        if self.proof_freshness.proof_freshness_slo_hours == 0
            || self.proof_freshness.last_proof_refresh.trim().is_empty()
        {
            violations.push(EvidenceCardViolation::ProofFreshnessIncomplete);
        }

        if json_contains_forbidden_boundary_material(
            &serde_json::to_value(self).expect("evidence-card packet serializes"),
        ) {
            violations.push(EvidenceCardViolation::RawBoundaryMaterialInExport);
        }

        violations
    }

    /// Deterministic export-safe JSON.
    ///
    /// # Panics
    ///
    /// Panics only if serializing this metadata-only packet fails.
    pub fn export_safe_json(&self) -> String {
        serde_json::to_string_pretty(self).expect("evidence-card packet serializes")
    }

    /// Deterministic Markdown summary for support, review, or release handoff.
    pub fn render_markdown_summary(&self) -> String {
        let stale_evidence = self
            .evidence_rows
            .iter()
            .filter(|row| row.freshness.is_stale())
            .count();
        let blocking_findings = self
            .finding_cards
            .iter()
            .filter(|row| row.severity == FindingSeverityClass::Blocking)
            .count();
        let detached_bindings = self
            .review_pack_bindings
            .iter()
            .filter(|row| row.binding_state.requires_detach_label())
            .count();

        let mut out = String::new();
        out.push_str(
            "# AI Review Evidence, Finding Cards, and Review-Pack Change-Object Integration\n\n",
        );
        out.push_str(&format!("- Packet: `{}`\n", self.packet_id));
        out.push_str(&format!("- Surface: `{}`\n", self.surface_label));
        out.push_str(&format!(
            "- Evidence rows: {} ({} stale or superseded and labeled)\n",
            self.evidence_rows.len(),
            stale_evidence
        ));
        out.push_str(&format!(
            "- Finding cards: {} ({} blocking)\n",
            self.finding_cards.len(),
            blocking_findings
        ));
        out.push_str(&format!(
            "- Review-pack bindings: {} ({} detached and relabeled)\n",
            self.review_pack_bindings.len(),
            detached_bindings
        ));
        out.push_str(&format!(
            "- Proof freshness SLO: {} hours (last refresh: {})\n",
            self.proof_freshness.proof_freshness_slo_hours, self.proof_freshness.last_proof_refresh
        ));

        out.push_str("\n## Evidence\n\n");
        for row in &self.evidence_rows {
            out.push_str(&format!(
                "- `{}` ({}): freshness `{}` — {}\n",
                row.evidence_id,
                row.source.as_str(),
                row.freshness.as_str(),
                row.provenance_label
            ));
        }

        out.push_str("\n## Finding cards\n\n");
        for row in &self.finding_cards {
            out.push_str(&format!(
                "- `{}` [{}/{}] → anchor `{}`, evidence `{}`, apply `{}`, change `{}`\n",
                row.finding_id,
                row.severity.as_str(),
                row.status.as_str(),
                row.anchor_id,
                row.evidence_ref,
                row.apply_posture.as_str(),
                row.change_object_ref
            ));
        }

        out.push_str("\n## Review-pack / change-object bindings\n\n");
        for row in &self.review_pack_bindings {
            out.push_str(&format!(
                "- pack `{}` ↔ change `{}`: {} ({})\n",
                row.review_pack_id,
                row.change_object_id,
                row.binding_state.as_str(),
                row.required_check_coverage.as_str()
            ));
        }
        out
    }
}

/// Errors emitted when reading the checked-in AI review-evidence finding-card export.
#[derive(Debug)]
pub enum EvidenceCardArtifactError {
    /// Support export failed to parse.
    SupportExport(serde_json::Error),
    /// Support export failed validation.
    Validation(Vec<EvidenceCardViolation>),
}

impl fmt::Display for EvidenceCardArtifactError {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::SupportExport(error) => {
                write!(formatter, "evidence-card export parse failed: {error}")
            }
            Self::Validation(violations) => {
                let tokens = violations
                    .iter()
                    .map(|violation| violation.as_str())
                    .collect::<Vec<_>>()
                    .join(",");
                write!(
                    formatter,
                    "evidence-card export failed validation: {tokens}"
                )
            }
        }
    }
}

impl Error for EvidenceCardArtifactError {}

/// Validation failures emitted by [`EvidenceCardPacket::validate`].
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum EvidenceCardViolation {
    /// Packet record kind is wrong.
    WrongRecordKind,
    /// Packet schema version is wrong.
    WrongSchemaVersion,
    /// Required identity field is missing.
    MissingIdentity,
    /// Source contract refs are incomplete.
    MissingSourceContracts,
    /// No evidence rows are present.
    EvidenceRowsMissing,
    /// An evidence row is incomplete.
    EvidenceRowIncomplete,
    /// Two evidence rows share an id.
    EvidenceIdDuplicated,
    /// No finding cards are present.
    FindingCardsMissing,
    /// A finding card is incomplete.
    FindingCardIncomplete,
    /// A finding card cites evidence that is not present.
    FindingEvidenceRefMissing,
    /// A blocked-apply card is missing its block-reason label.
    ApplyBlockLabelMissing,
    /// A finding card is scoped to a change object that no binding covers.
    FindingChangeObjectUnbound,
    /// No review-pack bindings are present.
    ReviewPackBindingsMissing,
    /// A review-pack binding is incomplete.
    ReviewPackBindingIncomplete,
    /// A detached binding is missing its detach label.
    DetachLabelMissing,
    /// No downgrade triggers are present.
    DowngradeTriggersMissing,
    /// No consumer surfaces are present.
    ConsumerSurfacesMissing,
    /// Trust review does not satisfy required invariants.
    TrustReviewIncomplete,
    /// Consumer projection does not satisfy required invariants.
    ConsumerProjectionIncomplete,
    /// Proof freshness block is incomplete.
    ProofFreshnessIncomplete,
    /// Export contains raw boundary material.
    RawBoundaryMaterialInExport,
}

impl EvidenceCardViolation {
    /// Stable token used in tests and support exports.
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::WrongRecordKind => "wrong_record_kind",
            Self::WrongSchemaVersion => "wrong_schema_version",
            Self::MissingIdentity => "missing_identity",
            Self::MissingSourceContracts => "missing_source_contracts",
            Self::EvidenceRowsMissing => "evidence_rows_missing",
            Self::EvidenceRowIncomplete => "evidence_row_incomplete",
            Self::EvidenceIdDuplicated => "evidence_id_duplicated",
            Self::FindingCardsMissing => "finding_cards_missing",
            Self::FindingCardIncomplete => "finding_card_incomplete",
            Self::FindingEvidenceRefMissing => "finding_evidence_ref_missing",
            Self::ApplyBlockLabelMissing => "apply_block_label_missing",
            Self::FindingChangeObjectUnbound => "finding_change_object_unbound",
            Self::ReviewPackBindingsMissing => "review_pack_bindings_missing",
            Self::ReviewPackBindingIncomplete => "review_pack_binding_incomplete",
            Self::DetachLabelMissing => "detach_label_missing",
            Self::DowngradeTriggersMissing => "downgrade_triggers_missing",
            Self::ConsumerSurfacesMissing => "consumer_surfaces_missing",
            Self::TrustReviewIncomplete => "trust_review_incomplete",
            Self::ConsumerProjectionIncomplete => "consumer_projection_incomplete",
            Self::ProofFreshnessIncomplete => "proof_freshness_incomplete",
            Self::RawBoundaryMaterialInExport => "raw_boundary_material_in_export",
        }
    }
}

/// Reads and validates the checked-in stable AI review-evidence finding-card export.
pub fn current_evidence_card_export() -> Result<EvidenceCardPacket, EvidenceCardArtifactError> {
    let packet: EvidenceCardPacket = serde_json::from_str(include_str!(concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/../../artifacts/review/m5/ship_ai_review_evidence_finding_cards_and_review_pack_integration_with_change_objects/support_export.json"
    )))
    .map_err(EvidenceCardArtifactError::SupportExport)?;
    let violations = packet.validate();
    if violations.is_empty() {
        Ok(packet)
    } else {
        Err(EvidenceCardArtifactError::Validation(violations))
    }
}

fn validate_source_contracts(
    packet: &EvidenceCardPacket,
    violations: &mut Vec<EvidenceCardViolation>,
) {
    let refs: BTreeSet<&str> = packet
        .source_contract_refs
        .iter()
        .map(String::as_str)
        .collect();
    for required in [
        EVIDENCE_CARD_SCHEMA_REF,
        EVIDENCE_CARD_DOC_REF,
        EVIDENCE_CARD_AI_EVIDENCE_CONTRACT_REF,
        EVIDENCE_CARD_REVIEW_PACK_CONTRACT_REF,
        EVIDENCE_CARD_CHANGE_OBJECT_CONTRACT_REF,
    ] {
        if !refs.contains(required) {
            violations.push(EvidenceCardViolation::MissingSourceContracts);
            return;
        }
    }
}

fn validate_evidence_rows(
    packet: &EvidenceCardPacket,
    violations: &mut Vec<EvidenceCardViolation>,
) {
    if packet.evidence_rows.is_empty() {
        violations.push(EvidenceCardViolation::EvidenceRowsMissing);
        return;
    }

    let mut seen: BTreeSet<&str> = BTreeSet::new();
    for row in &packet.evidence_rows {
        if row.evidence_id.trim().is_empty()
            || row.provenance_label.trim().is_empty()
            || row.summary_label.trim().is_empty()
            || row.source_contract_refs.is_empty()
        {
            violations.push(EvidenceCardViolation::EvidenceRowIncomplete);
        }
        if !row.evidence_id.trim().is_empty() && !seen.insert(row.evidence_id.as_str()) {
            violations.push(EvidenceCardViolation::EvidenceIdDuplicated);
        }
    }
}

fn validate_bindings(packet: &EvidenceCardPacket, violations: &mut Vec<EvidenceCardViolation>) {
    if packet.review_pack_bindings.is_empty() {
        violations.push(EvidenceCardViolation::ReviewPackBindingsMissing);
        return;
    }

    for row in &packet.review_pack_bindings {
        if row.review_pack_id.trim().is_empty()
            || row.change_object_id.trim().is_empty()
            || row.attribution_label.trim().is_empty()
        {
            violations.push(EvidenceCardViolation::ReviewPackBindingIncomplete);
        }
        if row.binding_state.requires_detach_label() && row.detach_label.trim().is_empty() {
            violations.push(EvidenceCardViolation::DetachLabelMissing);
        }
    }
}

fn validate_finding_cards(
    packet: &EvidenceCardPacket,
    violations: &mut Vec<EvidenceCardViolation>,
) {
    if packet.finding_cards.is_empty() {
        violations.push(EvidenceCardViolation::FindingCardsMissing);
        return;
    }

    let evidence_ids: BTreeSet<&str> = packet
        .evidence_rows
        .iter()
        .map(|row| row.evidence_id.as_str())
        .collect();
    let bound_change_objects: BTreeSet<&str> = packet
        .review_pack_bindings
        .iter()
        .map(|row| row.change_object_id.as_str())
        .collect();

    for row in &packet.finding_cards {
        if row.finding_id.trim().is_empty()
            || row.anchor_id.trim().is_empty()
            || row.title_label.trim().is_empty()
            || row.evidence_ref.trim().is_empty()
            || row.change_object_ref.trim().is_empty()
        {
            violations.push(EvidenceCardViolation::FindingCardIncomplete);
        }
        if !row.evidence_ref.trim().is_empty() && !evidence_ids.contains(row.evidence_ref.as_str())
        {
            violations.push(EvidenceCardViolation::FindingEvidenceRefMissing);
        }
        if !row.change_object_ref.trim().is_empty()
            && !bound_change_objects.contains(row.change_object_ref.as_str())
        {
            violations.push(EvidenceCardViolation::FindingChangeObjectUnbound);
        }
        if row.apply_posture.requires_block_label() && row.apply_block_label.trim().is_empty() {
            violations.push(EvidenceCardViolation::ApplyBlockLabelMissing);
        }
    }
}

/// Heuristic that rejects obviously forbidden material in export-safe JSON.
fn json_contains_forbidden_boundary_material(value: &serde_json::Value) -> bool {
    match value {
        serde_json::Value::String(s) => {
            let lower = s.to_lowercase();
            lower.contains("api_key")
                || lower.contains("password")
                || lower.contains("secret")
                || lower.contains("bearer ")
        }
        serde_json::Value::Array(arr) => arr.iter().any(json_contains_forbidden_boundary_material),
        serde_json::Value::Object(map) => {
            map.values().any(json_contains_forbidden_boundary_material)
        }
        _ => false,
    }
}
