//! AI review evidence, finding cards, and review-pack integration with change objects.
//!
//! This module implements the canonical M5 truth packet that binds AI-produced
//! review evidence to the finding cards a reviewer reads and to the review pack
//! and explicit change objects those cards are scoped against. It folds three
//! pillars into one export-safe record:
//!
//! - **AI review evidence** — each [`EvidenceCardEvidenceRow`] records a piece of
//!   review evidence with an explicit source class, freshness state, and
//!   provenance label, so a reviewer always knows what produced an observation
//!   and how fresh it is relative to the diff.
//! - **Finding cards** — each [`EvidenceCardFindingRow`] is the card a reviewer
//!   reads: it carries a severity, a status, the durable anchor it attaches to,
//!   the evidence it cites, the apply posture for any suggestion, and the change
//!   object it is scoped to.
//! - **Review-pack integration with change objects** — each
//!   [`EvidenceCardReviewPackBindingRow`] binds a review pack to an explicit
//!   change object, records required-check coverage, and labels detachment rather
//!   than silently dropping the binding.
//!
//! The packet references the upstream AI-evidence, review-pack, and
//! change-object contracts by id rather than embedding their content. Raw diff
//! bodies, raw model prompts or completions, raw build logs, raw provider
//! payloads, credentials, and live provider responses stay outside the support
//! boundary.
//!
//! The boundary schema is
//! [`schemas/review/ship-ai-review-evidence-finding-cards-and-review-pack-integration-with-change-objects.schema.json`](../../../../schemas/review/ship-ai-review-evidence-finding-cards-and-review-pack-integration-with-change-objects.schema.json).
//! The contract doc is
//! [`docs/review/m5/ship_ai_review_evidence_finding_cards_and_review_pack_integration_with_change_objects.md`](../../../../docs/review/m5/ship_ai_review_evidence_finding_cards_and_review_pack_integration_with_change_objects.md).
//! The protected fixture directory is
//! [`fixtures/review/m5/ship_ai_review_evidence_finding_cards_and_review_pack_integration_with_change_objects/`](../../../../fixtures/review/m5/ship_ai_review_evidence_finding_cards_and_review_pack_integration_with_change_objects/).

