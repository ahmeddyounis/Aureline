/// Freshness of a piece of evidence relative to the diff it describes.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum EvidenceFreshnessClass {
    /// Evidence is current against the diff.
    Fresh,
    /// The diff advanced under the evidence; the evidence shows a stale-diff label.
    StaleDiff,
    /// Newer evidence superseded this row.
    Superseded,
    /// Freshness cannot be computed (for example, offline).
    Unavailable,
}

impl EvidenceFreshnessClass {
    /// Stable token recorded in the packet.
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Fresh => "fresh",
            Self::StaleDiff => "stale_diff",
            Self::Superseded => "superseded",
            Self::Unavailable => "unavailable",
        }
    }

    /// Whether this class represents stale or superseded evidence that must be labeled.
    pub const fn is_stale(self) -> bool {
        matches!(self, Self::StaleDiff | Self::Superseded)
    }
}

