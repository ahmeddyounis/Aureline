fn policy_context(
    epoch: &str,
    trust_state: TrustState,
    execution_context_id: Option<&str>,
) -> PolicyContext {
    PolicyContext {
        policy_epoch: epoch.to_owned(),
        trust_state,
        execution_context_id: execution_context_id.map(str::to_owned),
    }
}

fn label_ref(layer: SourceLayerClass) -> String {
    format!("label:source_layer:{}", layer.as_str())
}

