//! Deterministic seeded catalog used by the live shell form host, the
//! markdown report, and the JSON fixtures checked in under
//! `fixtures/ux/m3/parameter_forms_and_invocation_review/`.
//!
//! Every field is pinned to a constant so the seed mints the same record
//! on every host, every revision, every OS. Scenarios cover the six
//! workpackages the spec calls out (WP-01 / WP-05 / WP-07 / WP-08 / WP-11 /
//! WP-14): destructive bulk replace (template/generator wedge), debug
//! attach to running process (run/debug profiles), outbound request issue
//! (request workspace), policy-pinned recipe step (automation recipe),
//! secret-bearing publish (irreversible publish), and a repair flow that
//! is blocked on a runtime prompt.

