/// Shared identity model binding every knowledge-plane surface to the
/// same node/edge identity space, the same freshness/confidence
/// vocabulary, and the same out-of-scope count model.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct SharedIdentityModel {
    /// Stable identity-model ref.
    pub identity_model_ref: String,
    /// Canonical node ids visible to every consumer surface.
    pub node_ids: Vec<String>,
    /// Canonical edge ids visible to every consumer surface.
    pub edge_ids: Vec<String>,
    /// Freshness vocabulary ref (e.g. the freshness-propagation contract).
    pub freshness_vocabulary_ref: String,
    /// Confidence vocabulary ref (e.g. the freshness-propagation contract).
    pub confidence_vocabulary_ref: String,
    /// Out-of-scope count model ref (visible, hidden, out-of-scope, widen-action).
    pub out_of_scope_count_model_ref: String,
}

impl SharedIdentityModel {
    fn is_valid(&self) -> bool {
        !self.identity_model_ref.trim().is_empty()
            && !self.freshness_vocabulary_ref.trim().is_empty()
            && !self.confidence_vocabulary_ref.trim().is_empty()
            && !self.out_of_scope_count_model_ref.trim().is_empty()
    }

    fn node_set(&self) -> BTreeSet<&str> {
        self.node_ids.iter().map(String::as_str).collect()
    }

    fn edge_set(&self) -> BTreeSet<&str> {
        self.edge_ids.iter().map(String::as_str).collect()
    }
}

