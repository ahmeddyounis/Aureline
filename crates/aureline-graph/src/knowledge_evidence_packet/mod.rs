//! Citation-ready knowledge-plane evidence packet.
//!
//! This module is the graph-owned contract that finalises topology maps,
//! impact explorer cards, ownership cards, and architecture explainer
//! snapshots into a single export-safe truth source. It binds the v24
//! promise that every claimed stable knowledge-plane row:
//!
//! - Shares one node/edge identity space, one freshness/confidence
//!   vocabulary, and one out-of-scope count model across topology
//!   canvases, list/table fallbacks, breadcrumbs, AI inspectors,
//!   onboarding tours, and review explainer cards.
//! - Never collapses `outside current slice` into `no impact found`.
//!   An impact card with hidden or out-of-scope counts must pin the
//!   exact widen action needed to compute a broader answer.
//! - Distinguishes curated first-party ownership, policy-derived
//!   ownership, imported provider metadata, and heuristic inference
//!   instead of merging every human or system role into one generic
//!   `owner` field.
//! - Pins cited refs, generated-versus-curated source class, freshness,
//!   confidence, and evidence-opening actions on every explainer
//!   snapshot. Generated prose may never ship without citations or an
//!   explicit inference label.
//!
//! The freshness and confidence vocabularies come from
//! [`crate::freshness_propagation_packet`] so the knowledge-plane
//! contract does not invent its own freshness or confidence states.
//!
//! The packet is intentionally metadata-only — it carries no raw node
//! bodies, no raw provider payloads, no secrets, and no ambient
//! credentials — and is read verbatim by the topology canvas, list/
//! table fallback, breadcrumb surface, AI inspector, onboarding tour,
//! review explainer card, CLI/headless emitter, support export, and
//! the release proof index instead of reconstructing knowledge-plane
//! truth from row-local prose.

