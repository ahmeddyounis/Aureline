/// Graph-owned packet for citation-ready knowledge-plane evidence.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct KnowledgeEvidencePacket {
    /// Stable record kind.
    pub record_kind: String,
    /// Schema version.
    pub schema_version: u32,
    /// Stable packet id.
    pub packet_id: String,
    /// Claimed workflow or surface id.
    pub workflow_or_surface_id: String,
    /// Packet capture timestamp.
    pub generated_at: String,
    /// Captured-vs-live status.
    pub captured_vs_live: CapturedVsLiveClass,
    /// Shared identity model binding every consumer surface.
    pub shared_identity_model: SharedIdentityModel,
    /// Topology view bound to the shared identity model.
    pub topology_view: TopologyView,
    /// Impact cards bound to the shared identity model.
    #[serde(default)]
    pub impact_cards: Vec<ImpactCard>,
    /// Ownership cards bound to the shared identity model.
    #[serde(default)]
    pub ownership_cards: Vec<OwnershipCard>,
    /// Explainer snapshots bound to the shared identity model.
    #[serde(default)]
    pub explainer_snapshots: Vec<ExplainerSnapshot>,
    /// Consumer projections preserving this packet.
    #[serde(default)]
    pub consumer_projections: Vec<KnowledgeConsumerProjection>,
    /// Source contract refs consumed by the packet.
    #[serde(default)]
    pub source_contract_refs: Vec<String>,
    /// Derived promotion state.
    pub promotion_state: KnowledgePromotionState,
    /// Validation findings captured at materialization.
    #[serde(default)]
    pub validation_findings: Vec<KnowledgeValidationFinding>,
}

impl KnowledgeEvidencePacket {
    /// Materialize a packet and record derived validation findings.
    pub fn materialize(input: KnowledgeEvidencePacketInput) -> Self {
        let mut packet = Self {
            record_kind: KNOWLEDGE_EVIDENCE_PACKET_RECORD_KIND.to_owned(),
            schema_version: KNOWLEDGE_EVIDENCE_PACKET_SCHEMA_VERSION,
            packet_id: input.packet_id,
            workflow_or_surface_id: input.workflow_or_surface_id,
            generated_at: input.generated_at,
            captured_vs_live: input.captured_vs_live,
            shared_identity_model: input.shared_identity_model,
            topology_view: input.topology_view,
            impact_cards: input.impact_cards,
            ownership_cards: input.ownership_cards,
            explainer_snapshots: input.explainer_snapshots,
            consumer_projections: input.consumer_projections,
            source_contract_refs: input.source_contract_refs,
            promotion_state: KnowledgePromotionState::Stable,
            validation_findings: Vec::new(),
        };
        let findings = packet.derived_findings(false);
        packet.promotion_state = promotion_state_for_findings(&findings);
        packet.validation_findings = findings;
        packet
    }

    /// Re-validate the packet against stable knowledge-plane invariants.
    pub fn validate(&self) -> Vec<KnowledgeValidationFinding> {
        self.derived_findings(true)
    }

    /// True when this packet has no blocker-level finding.
    pub fn is_stable(&self) -> bool {
        !self
            .validate()
            .iter()
            .any(|finding| finding.severity == KnowledgeFindingSeverity::Blocker)
    }

    /// Returns true when a consumer projection preserves this packet for `surface`.
    pub fn has_projection_for(&self, surface: KnowledgeConsumerSurface) -> bool {
        self.consumer_projections.iter().any(|projection| {
            projection.consumer_surface == surface
                && projection.preserves_truth_for(&self.packet_id)
        })
    }

    /// Returns the unique ownership-class tokens carried across ownership cards.
    pub fn ownership_class_tokens(&self) -> Vec<&'static str> {
        let mut set = BTreeSet::new();
        for card in &self.ownership_cards {
            set.insert(card.ownership_class);
        }
        set.into_iter().map(OwnershipClass::as_str).collect()
    }

    /// Returns the unique no-impact state tokens carried across impact cards.
    pub fn no_impact_state_tokens(&self) -> Vec<&'static str> {
        let mut set = BTreeSet::new();
        for card in &self.impact_cards {
            set.insert(card.no_impact_state);
        }
        set.into_iter().map(NoImpactState::as_str).collect()
    }

    /// Returns the unique explainer source-class tokens carried across snapshots.
    pub fn explainer_source_class_tokens(&self) -> Vec<&'static str> {
        let mut set = BTreeSet::new();
        for snapshot in &self.explainer_snapshots {
            set.insert(snapshot.source_class);
        }
        set.into_iter().map(ExplainerSourceClass::as_str).collect()
    }

    /// Build a support export wrapping the exact product packet.
    pub fn support_export(
        &self,
        export_id: impl Into<String>,
        exported_at: impl Into<String>,
    ) -> KnowledgeEvidencePacketSupportExport {
        KnowledgeEvidencePacketSupportExport {
            record_kind: KNOWLEDGE_EVIDENCE_PACKET_SUPPORT_EXPORT_RECORD_KIND.to_owned(),
            schema_version: KNOWLEDGE_EVIDENCE_PACKET_SCHEMA_VERSION,
            export_id: export_id.into(),
            export_packet_id_ref: self.packet_id.clone(),
            exported_at: exported_at.into(),
            raw_private_material_excluded: true,
            ambient_authority_excluded: true,
            export_packet: self.clone(),
        }
    }

    fn derived_findings(&self, include_record_fields: bool) -> Vec<KnowledgeValidationFinding> {
        let mut findings = Vec::new();

        if include_record_fields && self.record_kind != KNOWLEDGE_EVIDENCE_PACKET_RECORD_KIND {
            findings.push(KnowledgeValidationFinding::new(
                KnowledgeFindingKind::WrongRecordKind,
                KnowledgeFindingSeverity::Blocker,
                "knowledge-evidence packet has the wrong record kind",
            ));
        }
        if include_record_fields && self.schema_version != KNOWLEDGE_EVIDENCE_PACKET_SCHEMA_VERSION
        {
            findings.push(KnowledgeValidationFinding::new(
                KnowledgeFindingKind::WrongSchemaVersion,
                KnowledgeFindingSeverity::Blocker,
                "knowledge-evidence packet has the wrong schema version",
            ));
        }
        if self.packet_id.trim().is_empty()
            || self.workflow_or_surface_id.trim().is_empty()
            || self.generated_at.trim().is_empty()
        {
            findings.push(KnowledgeValidationFinding::new(
                KnowledgeFindingKind::MissingPacketIdentity,
                KnowledgeFindingSeverity::Blocker,
                "packet id, workflow id, and capture timestamp are required",
            ));
        }
        if !self.shared_identity_model.is_valid() {
            findings.push(KnowledgeValidationFinding::new(
                KnowledgeFindingKind::MissingSharedIdentityModel,
                KnowledgeFindingSeverity::Blocker,
                "shared identity model must declare identity, freshness, confidence, and out-of-scope refs",
            ));
        }

        if self.topology_view.view_id.trim().is_empty() {
            findings.push(KnowledgeValidationFinding::new(
                KnowledgeFindingKind::MissingTopologyView,
                KnowledgeFindingSeverity::Blocker,
                "topology view must declare a view id",
            ));
        }

        let node_set = self.shared_identity_model.node_set();
        let edge_set = self.shared_identity_model.edge_set();
        for node in &self.topology_view.nodes {
            if !node_set.contains(node.node_id.as_str()) {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::NodeEdgeIdentityMismatch,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "topology node {} is not pinned by the shared identity model",
                        node.node_id
                    ),
                ));
            }
        }
        for edge in &self.topology_view.edges {
            if !edge_set.contains(edge.edge_id.as_str()) {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::NodeEdgeIdentityMismatch,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "topology edge {} is not pinned by the shared identity model",
                        edge.edge_id
                    ),
                ));
            }
            if !node_set.contains(edge.from_node_id.as_str())
                || !node_set.contains(edge.to_node_id.as_str())
            {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::NodeEdgeIdentityMismatch,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "topology edge {} references unpinned endpoint nodes",
                        edge.edge_id
                    ),
                ));
            }
        }

        if self.topology_view.visible_node_count != self.topology_view.nodes.len() {
            findings.push(KnowledgeValidationFinding::new(
                KnowledgeFindingKind::OutOfScopeCountDropped,
                KnowledgeFindingSeverity::Blocker,
                "topology view visible node count does not match the projected node list",
            ));
        }
        if self.topology_view.visible_edge_count != self.topology_view.edges.len() {
            findings.push(KnowledgeValidationFinding::new(
                KnowledgeFindingKind::OutOfScopeCountDropped,
                KnowledgeFindingSeverity::Blocker,
                "topology view visible edge count does not match the projected edge list",
            ));
        }
        if self.topology_view.requires_widen_action()
            && self
                .topology_view
                .widen_scope_action_ref
                .as_deref()
                .map(|reference| reference.trim().is_empty())
                .unwrap_or(true)
        {
            findings.push(KnowledgeValidationFinding::new(
                KnowledgeFindingKind::TopologyMissingWidenAction,
                KnowledgeFindingSeverity::Blocker,
                "topology view has out-of-scope counts but no widen-scope action ref",
            ));
        }

        for card in &self.impact_cards {
            if card.collapses_on_out_of_scope() {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::NoImpactCollapsedOnOutOfScopeImpact,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "impact card {} collapses out-of-scope impact into no_impact_in_workspace_or_slice",
                        card.card_id
                    ),
                ));
            }
            if card.no_impact_state.requires_widen_action()
                && card
                    .widen_action_ref
                    .as_deref()
                    .map(|reference| reference.trim().is_empty())
                    .unwrap_or(true)
            {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::ImpactCardMissingWidenAction,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "impact card {} discloses outside-slice impact but drops the widen-action ref",
                        card.card_id
                    ),
                ));
            }
            if !card.raw_boundary_material_excluded {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::RawBoundaryMaterialPresent,
                    KnowledgeFindingSeverity::Blocker,
                    format!("impact card {} admits raw boundary material", card.card_id),
                ));
            }
            if !node_set.contains(card.subject_node_id.as_str()) {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::NodeEdgeIdentityMismatch,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "impact card {} subject node {} is not pinned by the shared identity model",
                        card.card_id, card.subject_node_id
                    ),
                ));
            }
        }

        for card in &self.ownership_cards {
            if card.card_id.trim().is_empty() {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::OwnershipClassMissing,
                    KnowledgeFindingSeverity::Blocker,
                    "ownership card is missing its card id",
                ));
            }
            if card.ownership_class.requires_partiality_note()
                && card
                    .partiality_note
                    .as_deref()
                    .map(|note| note.trim().is_empty())
                    .unwrap_or(true)
            {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::OwnershipPartialityNoteMissing,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "ownership card {} uses {} but carries no partiality note",
                        card.card_id,
                        card.ownership_class.as_str()
                    ),
                ));
            }
            if !card.raw_boundary_material_excluded {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::RawBoundaryMaterialPresent,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "ownership card {} admits raw boundary material",
                        card.card_id
                    ),
                ));
            }
            if !node_set.contains(card.subject_node_id.as_str()) {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::NodeEdgeIdentityMismatch,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "ownership card {} subject node {} is not pinned by the shared identity model",
                        card.card_id, card.subject_node_id
                    ),
                ));
            }
        }

        for snapshot in &self.explainer_snapshots {
            if snapshot.snapshot_id.trim().is_empty() {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::ExplainerSourceClassMissing,
                    KnowledgeFindingSeverity::Blocker,
                    "explainer snapshot is missing its snapshot id",
                ));
            }
            if !snapshot.meets_citation_or_inference_requirement() {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::GeneratedExplainerMissingCitationsOrInference,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "explainer snapshot {} is generated but ships without citations or an inference label",
                        snapshot.snapshot_id
                    ),
                ));
            }
            if snapshot.evidence_opening_actions.is_empty() {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::ExplainerMissingEvidenceOpeningActions,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "explainer snapshot {} has no evidence-opening actions",
                        snapshot.snapshot_id
                    ),
                ));
            }
            if !snapshot.raw_boundary_material_excluded {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::RawBoundaryMaterialPresent,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "explainer snapshot {} admits raw boundary material",
                        snapshot.snapshot_id
                    ),
                ));
            }
            if !node_set.contains(snapshot.subject_node_id.as_str()) {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::NodeEdgeIdentityMismatch,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "explainer snapshot {} subject node {} is not pinned by the shared identity model",
                        snapshot.snapshot_id, snapshot.subject_node_id
                    ),
                ));
            }
        }

        for required_surface in KnowledgeConsumerSurface::REQUIRED {
            if !self.has_projection_for(required_surface) {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::MissingConsumerProjection,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "packet {} is missing a preserved {} projection",
                        self.packet_id,
                        required_surface.as_str()
                    ),
                ));
            }
        }
        for projection in &self.consumer_projections {
            if !projection.preserves_truth_for(&self.packet_id) {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::ConsumerProjectionDrift,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "projection {} does not preserve knowledge-plane truth",
                        projection.projection_ref
                    ),
                ));
            }
            if !projection.preserves_node_edge_identity {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::NodeEdgeIdentityDropped,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "projection {} drops the node/edge identity binding",
                        projection.projection_ref
                    ),
                ));
            }
            if !projection.preserves_freshness_vocabulary {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::FreshnessVocabularyDropped,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "projection {} drops the freshness vocabulary",
                        projection.projection_ref
                    ),
                ));
            }
            if !projection.preserves_confidence_vocabulary {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::ConfidenceVocabularyDropped,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "projection {} drops the confidence vocabulary",
                        projection.projection_ref
                    ),
                ));
            }
            if !projection.preserves_out_of_scope_count_model {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::OutOfScopeCountModelDropped,
                    KnowledgeFindingSeverity::Blocker,
                    format!(
                        "projection {} drops the out-of-scope count model",
                        projection.projection_ref
                    ),
                ));
            }
        }

        if include_record_fields {
            let mut without_promotion = findings.clone();
            without_promotion.retain(|finding| {
                finding.finding_kind != KnowledgeFindingKind::PromotionStateMismatch
            });
            let derived = promotion_state_for_findings(&without_promotion);
            if self.promotion_state != derived {
                findings.push(KnowledgeValidationFinding::new(
                    KnowledgeFindingKind::PromotionStateMismatch,
                    KnowledgeFindingSeverity::Blocker,
                    "stored promotion state does not match derived findings",
                ));
            }
        }

        findings
    }
}

fn promotion_state_for_findings(
    findings: &[KnowledgeValidationFinding],
) -> KnowledgePromotionState {
    if findings
        .iter()
        .any(|finding| finding.severity == KnowledgeFindingSeverity::Blocker)
    {
        KnowledgePromotionState::BlocksStable
    } else if findings
        .iter()
        .any(|finding| finding.severity == KnowledgeFindingSeverity::Warning)
    {
        KnowledgePromotionState::NarrowedBelowStable
    } else {
        KnowledgePromotionState::Stable
    }
}

/// Support-export wrapper preserving the product knowledge-plane packet verbatim.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct KnowledgeEvidencePacketSupportExport {
    /// Stable record kind.
    pub record_kind: String,
    /// Schema version.
    pub schema_version: u32,
    /// Stable export id.
    pub export_id: String,
    /// Export packet id preserved by the export.
    pub export_packet_id_ref: String,
    /// Export timestamp.
    pub exported_at: String,
    /// True when raw private material is excluded.
    pub raw_private_material_excluded: bool,
    /// True when ambient credentials / authority are excluded.
    pub ambient_authority_excluded: bool,
    /// Exact product packet preserved by the export.
    pub export_packet: KnowledgeEvidencePacket,
}

impl KnowledgeEvidencePacketSupportExport {
    /// True when the export preserves the same packet id safely.
    pub fn is_export_safe(&self) -> bool {
        self.record_kind == KNOWLEDGE_EVIDENCE_PACKET_SUPPORT_EXPORT_RECORD_KIND
            && self.schema_version == KNOWLEDGE_EVIDENCE_PACKET_SCHEMA_VERSION
            && self.export_packet_id_ref == self.export_packet.packet_id
            && self.raw_private_material_excluded
            && self.ambient_authority_excluded
            && self.export_packet.validate().is_empty()
    }
}

/// Errors emitted when reading the checked-in stable knowledge-plane packet.
#[derive(Debug)]
pub enum KnowledgeEvidenceArtifactError {
    /// Packet failed to parse.
    Packet(serde_json::Error),
    /// Packet failed validation.
    Validation(Vec<KnowledgeValidationFinding>),
}

impl fmt::Display for KnowledgeEvidenceArtifactError {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Packet(error) => write!(
                formatter,
                "graph knowledge-evidence packet parse failed: {error}"
            ),
            Self::Validation(findings) => {
                let tokens = findings
                    .iter()
                    .map(|finding| finding.finding_kind.as_str())
                    .collect::<Vec<_>>()
                    .join(",");
                write!(
                    formatter,
                    "graph knowledge-evidence packet failed validation: {tokens}"
                )
            }
        }
    }
}

impl Error for KnowledgeEvidenceArtifactError {}

/// Returns the checked-in stable knowledge-plane evidence packet.
///
/// # Errors
///
/// Returns an artifact error if the checked-in packet does not parse or validate.
pub fn current_stable_knowledge_evidence_packet(
) -> Result<KnowledgeEvidencePacket, KnowledgeEvidenceArtifactError> {
    let packet: KnowledgeEvidencePacket = serde_json::from_str(include_str!(concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/../../artifacts/search/m4/knowledge_evidence_packet.json"
    )))
    .map_err(KnowledgeEvidenceArtifactError::Packet)?;
    let findings = packet.validate();
    if findings.is_empty() {
        Ok(packet)
    } else {
        Err(KnowledgeEvidenceArtifactError::Validation(findings))
    }
}

