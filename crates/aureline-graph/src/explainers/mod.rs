impl GraphStore {
    /// Builds an alpha impact explainer packet from an impact-seed query request.
    ///
    /// The packet preserves graph query identity and adds the evidence card,
    /// workset-scope disclosure, out-of-scope count, and non-canvas fallback
    /// required by graph-backed understanding surfaces.
    ///
    /// # Errors
    ///
    /// Returns [`GraphExplainerError::UnsupportedQueryClass`] when `request`
    /// is not an impact-seed request. Returns
    /// [`GraphExplainerError::MissingSubjectNodeId`] when the request does not
    /// carry a subject node id.
    pub fn build_impact_explainer_packet(
        &self,
        request: GraphQueryRequest,
        workset_scope: WorksetScopeDescriptor,
    ) -> Result<ImpactExplainerPacket, GraphExplainerError> {
        if request.query_class != GraphAlphaQueryClass::ImpactSeed {
            return Err(GraphExplainerError::UnsupportedQueryClass {
                expected: GraphAlphaQueryClass::ImpactSeed,
                actual: request.query_class,
            });
        }
        let subject_node_id = request
            .subject_node_id
            .clone()
            .ok_or(GraphExplainerError::MissingSubjectNodeId)?;
        let requested_scope_ids = request.scope_ids.clone();
        let envelope = self.query(request);
        Ok(ImpactExplainerPacket::from_graph_query_envelope(
            self,
            &envelope,
            subject_node_id,
            requested_scope_ids,
            workset_scope,
        ))
    }
}

impl ImpactExplainerPacket {
    fn from_graph_query_envelope(
        store: &GraphStore,
        envelope: &GraphQueryEnvelope,
        subject_node_id: String,
        requested_scope_ids: Vec<String>,
        workset_scope: WorksetScopeDescriptor,
    ) -> Self {
        let impact_edges = envelope
            .rows
            .iter()
            .filter(|row| row.edge_class == Some(EdgeClass::Impacts))
            .filter_map(|row| row.edge_id.as_deref())
            .filter_map(|edge_id| store.edge(edge_id))
            .collect::<Vec<_>>();
        let out_of_scope_count =
            matching_out_of_scope_impact_edges(store, &subject_node_id, &requested_scope_ids);

        let mut node_ids = BTreeSet::new();
        for row in &envelope.rows {
            if let Some(node_id) = &row.node_id {
                node_ids.insert(node_id.clone());
            }
        }
        for edge in &impact_edges {
            node_ids.insert(edge.from_node_id.clone());
            node_ids.insert(edge.to_node_id.clone());
        }

        let nodes = node_ids
            .iter()
            .filter_map(|node_id| store.node(node_id))
            .map(node_projection)
            .collect::<Vec<_>>();
        let edges = impact_edges
            .iter()
            .map(|edge| edge_projection(edge))
            .collect::<Vec<_>>();

        let impact_summary = impact_summary(&impact_edges, out_of_scope_count);
        let packet_id = format!("packet:impact_explainer:{}", envelope.query_request_id);
        let evidence_card = evidence_card_for_packet(store, envelope, &packet_id, &impact_summary);
        let visual_projection = VisualTopologyProjection {
            projection_id: format!(
                "projection:impact_explainer:{}:map",
                envelope.query_request_id
            ),
            presentation_mode: "map_canvas".to_owned(),
            nodes,
            edges,
            shared_identity_model: "graph_node_id_and_graph_edge_id".to_owned(),
        };
        let non_canvas_fallback =
            fallback_for_projection(store, envelope, &visual_projection, &impact_edges);

        Self {
            record_kind: GRAPH_IMPACT_EXPLAINER_PACKET_RECORD_KIND.to_owned(),
            schema_version: GRAPH_IMPACT_EXPLAINER_PACKET_SCHEMA_VERSION,
            packet_id,
            query_request_id: envelope.query_request_id.clone(),
            workspace_id: envelope.workspace_id.clone(),
            workspace_graph_id: envelope.workspace_graph_id.clone(),
            emitted_at: envelope.emitted_at.clone(),
            readiness: envelope.readiness.as_str().to_owned(),
            partial_truth_causes: envelope
                .partial_truth_causes
                .iter()
                .map(|cause| cause.as_str().to_owned())
                .collect(),
            subject_node_id,
            workset_scope,
            coverage_claim:
                "stored impact edges for the active workset only; full transitive repo impact is not claimed"
                    .to_owned(),
            impact_summary,
            evidence_card,
            visual_projection,
            non_canvas_fallback,
        }
    }
}

fn impact_summary(edges: &[&GraphEdge], out_of_scope_count: usize) -> ImpactSummary {
    let exact_edge_count = edges
        .iter()
        .filter(|edge| evidence_class(edge) == ImpactEdgeEvidenceClass::Exact)
        .count();
    let imported_edge_count = edges
        .iter()
        .filter(|edge| evidence_class(edge) == ImpactEdgeEvidenceClass::Imported)
        .count();
    let heuristic_edge_count = edges
        .iter()
        .filter(|edge| evidence_class(edge) == ImpactEdgeEvidenceClass::Heuristic)
        .count();
    let impacted_node_count = edges
        .iter()
        .map(|edge| edge.to_node_id.as_str())
        .collect::<BTreeSet<_>>()
        .len();
    let no_impact_found = edges.is_empty();
    let no_impact_reason = match (no_impact_found, out_of_scope_count) {
        (true, 0) => Some("no_stored_impact_edges_in_active_scope".to_owned()),
        (true, _) => Some("impact_edges_exist_outside_active_scope".to_owned()),
        (false, _) => None,
    };

    ImpactSummary {
        impacted_node_count,
        visible_impact_edge_count: edges.len(),
        out_of_scope_count,
        exact_edge_count,
        imported_edge_count,
        heuristic_edge_count,
        no_impact_found,
        no_impact_reason,
    }
}

fn matching_out_of_scope_impact_edges(
    store: &GraphStore,
    subject_node_id: &str,
    requested_scope_ids: &[String],
) -> usize {
    if requested_scope_ids.is_empty() {
        return 0;
    }
    store
        .all_edges()
        .filter(|edge| {
            edge.edge_class == EdgeClass::Impacts
                && edge
                    .query_family_tags
                    .contains(&QueryFamilyTag::ImpactExplorer)
                && (edge.from_node_id == subject_node_id || edge.to_node_id == subject_node_id)
                && !edge
                    .scope_refs
                    .iter()
                    .any(|scope| requested_scope_ids.contains(&scope.scope_id))
        })
        .count()
}

fn evidence_card_for_packet(
    store: &GraphStore,
    envelope: &GraphQueryEnvelope,
    packet_id: &str,
    impact_summary: &ImpactSummary,
) -> EvidenceCard {
    let citations = citations_for_envelope(store, envelope);
    let generated_vs_curated_source = if citations
        .iter()
        .any(|citation| citation.source_kind == ExplainerSourceKind::Generated)
    {
        ExplainerSourceKind::Generated
    } else {
        ExplainerSourceKind::Curated
    };
    let cited_file_count = citations
        .iter()
        .filter(|citation| citation.file_path.is_some())
        .count();
    let cited_symbol_count = citations
        .iter()
        .filter(|citation| citation.symbol_ref.is_some())
        .count();
    let cited_doc_count = citations
        .iter()
        .filter(|citation| citation.doc_ref.is_some())
        .count();

    EvidenceCard {
        record_kind: "graph_explainer_evidence_card".to_owned(),
        evidence_card_id: format!("{packet_id}:evidence_card"),
        generated_vs_curated_source,
        freshness: packet_freshness(&citations),
        confidence: packet_confidence(&citations),
        cited_file_count,
        cited_symbol_count,
        cited_doc_count,
        citation_evidence_views: citations
            .iter()
            .filter_map(|citation| docs_citation_view_for_evidence(citation, packet_id))
            .collect(),
        citations,
        open_detail_actions: open_detail_actions(envelope, packet_id, impact_summary),
    }
}

fn citations_for_envelope(
    store: &GraphStore,
    envelope: &GraphQueryEnvelope,
) -> Vec<EvidenceCitation> {
    let mut citations = Vec::new();
    for row in &envelope.rows {
        if let Some(edge_id) = &row.edge_id {
            if let Some(edge) = store.edge(edge_id) {
                citations.push(edge_citation(citations.len(), edge));
            }
        }
        if let Some(node_id) = &row.node_id {
            if let Some(node) = store.node(node_id) {
                citations.push(node_citation(citations.len(), node));
            }
        }
    }
    citations
}

fn open_detail_actions(
    envelope: &GraphQueryEnvelope,
    packet_id: &str,
    impact_summary: &ImpactSummary,
) -> Vec<OpenDetailAction> {
    let mut actions = Vec::new();
    for row in &envelope.rows {
        if let Some(edge_id) = &row.edge_id {
            actions.push(OpenDetailAction {
                action_id: format!("action:{edge_id}:inspect_graph_relation"),
                action_class: OpenDetailActionClass::InspectGraphRelation,
                subject_ref: edge_id.clone(),
                preserves_scope: true,
            });
        }
        if let Some(node_id) = &row.node_id {
            actions.push(OpenDetailAction {
                action_id: format!("action:{node_id}:open_source_at_anchor"),
                action_class: OpenDetailActionClass::OpenSourceAtAnchor,
                subject_ref: node_id.clone(),
                preserves_scope: true,
            });
        }
    }
    actions.push(OpenDetailAction {
        action_id: format!("action:{packet_id}:open_topology_table"),
        action_class: OpenDetailActionClass::OpenTopologyTable,
        subject_ref: packet_id.to_owned(),
        preserves_scope: true,
    });
    if impact_summary.out_of_scope_count > 0 {
        actions.push(OpenDetailAction {
            action_id: format!("action:{packet_id}:widen_scope"),
            action_class: OpenDetailActionClass::WidenScope,
            subject_ref: packet_id.to_owned(),
            preserves_scope: false,
        });
    }
    actions.push(OpenDetailAction {
        action_id: format!("action:{packet_id}:export_evidence_card"),
        action_class: OpenDetailActionClass::ExportEvidenceCard,
        subject_ref: format!("{packet_id}:evidence_card"),
        preserves_scope: true,
    });
    actions
}

fn fallback_for_projection(
    store: &GraphStore,
    envelope: &GraphQueryEnvelope,
    visual_projection: &VisualTopologyProjection,
    edges: &[&GraphEdge],
) -> NonCanvasTopologyFallback {
    let rows = edges
        .iter()
        .map(|edge| {
            let from_label = store
                .node(&edge.from_node_id)
                .map(display_label_for_node)
                .unwrap_or_else(|| edge.from_node_id.clone());
            let to_label = store
                .node(&edge.to_node_id)
                .map(display_label_for_node)
                .unwrap_or_else(|| edge.to_node_id.clone());
            TopologyFallbackRow {
                edge_id: edge.edge_id.clone(),
                from_node_id: edge.from_node_id.clone(),
                from_label,
                edge_class: edge.edge_class.as_str().to_owned(),
                to_node_id: edge.to_node_id.clone(),
                to_label,
                evidence_state: edge.evidence.evidence_state.as_str().to_owned(),
                edge_evidence_class: evidence_class(edge),
                freshness: edge.evidence.freshness_frame.freshness.as_str().to_owned(),
                confidence: edge.evidence.confidence_level.as_str().to_owned(),
            }
        })
        .collect();

    NonCanvasTopologyFallback {
        projection_id: format!(
            "projection:impact_explainer:{}:edge_table",
            envelope.query_request_id
        ),
        presentation_mode: "edge_table_alternate".to_owned(),
        rows,
        shared_node_ids: visual_projection
            .nodes
            .iter()
            .map(|node| node.node_id.clone())
            .collect(),
        shared_edge_ids: visual_projection
            .edges
            .iter()
            .map(|edge| edge.edge_id.clone())
            .collect(),
    }
}

