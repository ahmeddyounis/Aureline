/// Schema version for alpha impact explainer packets.
pub const GRAPH_IMPACT_EXPLAINER_PACKET_SCHEMA_VERSION: u32 = 1;

/// Stable record-kind tag carried by impact explainer packets.
pub const GRAPH_IMPACT_EXPLAINER_PACKET_RECORD_KIND: &str = "graph_impact_explainer_packet";

/// Error returned when an impact explainer packet cannot be built.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum GraphExplainerError {
    /// The request targeted a query class the impact explainer cannot consume.
    UnsupportedQueryClass {
        /// Query class required by this packet builder.
        expected: GraphAlphaQueryClass,
        /// Query class supplied by the caller.
        actual: GraphAlphaQueryClass,
    },
    /// The impact request did not identify a subject graph node.
    MissingSubjectNodeId,
}

/// Whether an explainer or citation was generated by a producer or curated by a source artifact.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ExplainerSourceKind {
    /// Producer-generated evidence such as an inferred graph edge.
    Generated,
    /// Curated or authoritative source evidence such as source files, docs, or ownership rules.
    Curated,
}

impl ExplainerSourceKind {
    /// Returns the stable token used in exported packets.
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Generated => "generated",
            Self::Curated => "curated",
        }
    }
}

/// Whether the active scope is fully loaded or intentionally sparse.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum WorksetScopeMode {
    /// The packet covers the declared scope fully.
    Full,
    /// The packet covers a sparse slice or hot-set subset.
    Sparse,
}

impl WorksetScopeMode {
    /// Returns the stable token used in exported packets.
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Full => "full",
            Self::Sparse => "sparse",
        }
    }
}

/// Where the scope boundary was sourced from.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum WorksetScopeSource {
    /// Local workspace state supplied the scope boundary.
    Local,
    /// Managed or provider state supplied the scope boundary.
    Managed,
}

impl WorksetScopeSource {
    /// Returns the stable token used in exported packets.
    pub const fn as_str(self) -> &'static str {
        match self {
            Self::Local => "local",
            Self::Managed => "managed",
        }
    }
}

