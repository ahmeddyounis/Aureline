/// Typed workset-scope disclosure carried by graph explainers.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct WorksetScopeDescriptor {
    /// Stable scope id that bounds the packet.
    pub scope_id: String,
    /// Scope class token copied from the graph scope vocabulary.
    pub scope_class: String,
    /// Included workspace roots or repository labels.
    pub included_roots_or_repos: Vec<String>,
    /// Whether the scope is full or sparse.
    pub scope_mode: WorksetScopeMode,
    /// Count of results hidden by workset, policy, or unloaded-scope boundaries.
    pub hidden_result_count: usize,
    /// Index coverage for the active scope.
    pub index_coverage: IndexCoverage,
    /// Whether the scope boundary came from local or managed state.
    pub scope_source: WorksetScopeSource,
}

impl WorksetScopeDescriptor {
    /// Builds a local full-scope descriptor.
    pub fn local_full(
        scope_id: impl Into<String>,
        scope_class: impl Into<String>,
        included_roots_or_repos: impl IntoIterator<Item = impl Into<String>>,
        covered_node_count: usize,
        covered_edge_count: usize,
    ) -> Self {
        Self {
            scope_id: scope_id.into(),
            scope_class: scope_class.into(),
            included_roots_or_repos: included_roots_or_repos
                .into_iter()
                .map(Into::into)
                .collect(),
            scope_mode: WorksetScopeMode::Full,
            hidden_result_count: 0,
            index_coverage: IndexCoverage {
                covered_node_count,
                covered_edge_count,
                not_loaded_count: 0,
                coverage_state: "fully_loaded".to_owned(),
            },
            scope_source: WorksetScopeSource::Local,
        }
    }

    /// Builds a local sparse-scope descriptor.
    pub fn local_sparse(
        scope_id: impl Into<String>,
        scope_class: impl Into<String>,
        included_roots_or_repos: impl IntoIterator<Item = impl Into<String>>,
        hidden_result_count: usize,
        covered_node_count: usize,
        covered_edge_count: usize,
        not_loaded_count: usize,
    ) -> Self {
        Self {
            scope_id: scope_id.into(),
            scope_class: scope_class.into(),
            included_roots_or_repos: included_roots_or_repos
                .into_iter()
                .map(Into::into)
                .collect(),
            scope_mode: WorksetScopeMode::Sparse,
            hidden_result_count,
            index_coverage: IndexCoverage {
                covered_node_count,
                covered_edge_count,
                not_loaded_count,
                coverage_state: "sparse_slice_loaded".to_owned(),
            },
            scope_source: WorksetScopeSource::Local,
        }
    }
}

