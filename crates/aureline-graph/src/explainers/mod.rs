//! Graph-backed impact explainer packets with evidence-card projections.
//!
//! The packet types in this module consume [`crate::GraphQueryEnvelope`]
//! values directly. They preserve graph node and edge identity while adding the
//! compact evidence card, workset-scope disclosure, out-of-scope counts, and
//! non-canvas fallback that understanding surfaces need before a full topology
//! renderer exists.

