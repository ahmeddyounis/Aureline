/// A user-/operator-facing truth surface that ingests canonical M5 evidence.
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum TruthSurface {
    /// In-product Help and the About / provenance card.
    HelpAbout,
    /// Service-health row (carries an operational contract state).
    ServiceHealth,
    /// Release-center family card.
    ReleaseCenter,
    /// Support-export row.
    SupportExport,
    /// Outward-facing public-truth pack.
    PublicTruthPack,
}

impl TruthSurface {
    /// Every truth surface the register must cover.
    pub const ALL: [Self; 5] = [
        Self::HelpAbout,
        Self::ServiceHealth,
        Self::ReleaseCenter,
        Self::SupportExport,
        Self::PublicTruthPack,
    ];
}

/// The M5 feature family a row ingests.
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum FamilyKind {
    /// Notebook and computational-document family.
    Notebook,
    /// Data-rich grid/query family.
    DataRich,
    /// AI-adjacent assistance family.
    AiAdjacent,
    /// Framework / language-pack family.
    Framework,
    /// Review and pipeline family.
    Review,
    /// Browser/mobile companion family.
    Companion,
    /// Admin-managed depth family.
    ManagedDepth,
}

impl FamilyKind {
    /// Every M5 feature family the register must cover.
    pub const ALL: [Self; 7] = [
        Self::Notebook,
        Self::DataRich,
        Self::AiAdjacent,
        Self::Framework,
        Self::Review,
        Self::Companion,
        Self::ManagedDepth,
    ];
}

/// The state a truth surface publishes for a family, projected from the canonical
/// family lifecycle state. Never wider than the source.
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum IngestState {
    /// Backed at the canonical claim ceiling; nothing narrowed.
    Current,
    /// Backing proof packet aged out of its freshness SLO.
    Stale,
    /// Narrowed below the claim ceiling for a structural reason (e.g. missing
    /// rollback path).
    Narrowed,
    /// A policy block prevents publishing at the claim ceiling.
    PolicyBlocked,
    /// Surfaced as preview-only because the family is still incomplete.
    PreviewOnly,
    /// Provisionally qualified on an active waiver.
    Underqualified,
}

impl IngestState {
    /// True when the surface is fully backed at the ceiling.
    pub const fn is_current(self) -> bool {
        matches!(self, Self::Current)
    }

    /// States that require the published label to sit strictly below the stable
    /// cutline. `Current` is at the ceiling and `Underqualified` may sit at the
    /// ceiling on a waiver, so both are excluded.
    pub const fn requires_below_cutline(self) -> bool {
        matches!(
            self,
            Self::Stale | Self::Narrowed | Self::PolicyBlocked | Self::PreviewOnly
        )
    }
}

/// The boundary posture surfaced on every row. Always explicit, never inferred.
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum PostureClass {
    /// Runs locally with no managed reachability dependency.
    LocalOnly,
    /// Mirrored through a managed provider/registry surface.
    Mirrored,
    /// Admin-managed; behavior governed by policy.
    Managed,
    /// Hands off to the system browser / companion device for high-risk steps.
    BrowserHandoff,
}

/// A reason a surface publishes a non-current ingestion state, readable without
/// internal notes.
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum IngestReason {
    /// The source family is on an active qualification waiver.
    SourceOnActiveWaiver,
    /// The source proof packet breached its freshness SLO.
    SourceProofPacketStale,
    /// The source family is structurally incomplete.
    SourceFamilyIncomplete,
    /// The source family has no verified rollback path.
    SourceRollbackPathMissing,
    /// The family is admin-managed and gated by policy.
    AdminPolicyManaged,
    /// The source family is narrowed for another recorded reason.
    SourceFamilyNarrowed,
}

/// One contradiction rule the ingestion register declares; the CI gate and this
/// model both honour it.
