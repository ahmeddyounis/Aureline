/// A structural or logical invariant the register violated.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case", tag = "code", content = "detail")]
pub enum IngestionViolation {
    /// Schema version did not match the supported version.
    SchemaVersionMismatch { found: u32 },
    /// Record kind tag did not match.
    RecordKindMismatch { found: String },
    /// A required string field was empty.
    EmptyField { entry_id: String, field: String },
    /// The published label is wider than the canonical claim ceiling.
    PublishedExceedsCeiling { entry_id: String },
    /// The published label does not equal the canonical effective label.
    PublishedNotCanonical { entry_id: String },
    /// The canonical effective label is wider than the canonical ceiling.
    SourceLabelInconsistent { entry_id: String },
    /// A current row carries narrowing reasons or is not at the ceiling.
    CurrentRowNotBacked { entry_id: String },
    /// A non-current row has no reasons.
    NarrowedRowWithoutReason { entry_id: String },
    /// A below-cutline state did not narrow below the stable cutline.
    NarrowedStateNotBelowCutline { entry_id: String },
    /// A service-health row is missing its contract state.
    ServiceHealthMissingContractState { entry_id: String },
    /// A non-service-health row carries a contract state.
    NonServiceHealthHasContractState { entry_id: String },
    /// A family ref claims release-blocking inconsistently with the declared set.
    ReleaseBlockingMismatch { entry_id: String },
    /// A declared family kind was never covered by any row.
    FamilyKindUncovered { family_kind: FamilyKind },
    /// A declared truth surface was never covered by any row.
    TruthSurfaceUncovered { truth_surface: TruthSurface },
    /// A family is missing a row for at least one truth surface.
    FamilySurfaceCoverageGap {
        family_kind: FamilyKind,
        truth_surface: TruthSurface,
    },
    /// The publication verdict did not match the computed verdict.
    PublicationVerdictMismatch { found: String, expected: String },
    /// The summary did not match the computed roll-up.
    SummaryMismatch { field: String },
}

impl M5TruthSurfaceIngestion {
    /// Validate every structural and logical invariant the register must hold.
    ///
    /// Returns an empty vector when the register is internally coherent. Clock and
    /// cross-artifact checks live in the CI gate; this method enforces invariants
    /// independent of the date and the neighbouring source packet.
    pub fn validate(&self) -> Vec<IngestionViolation> {
        let mut out = Vec::new();

        if self.schema_version != M5_TRUTH_SURFACE_INGESTION_SCHEMA_VERSION {
            out.push(IngestionViolation::SchemaVersionMismatch {
                found: self.schema_version,
            });
        }
        if self.record_kind != M5_TRUTH_SURFACE_INGESTION_RECORD_KIND {
            out.push(IngestionViolation::RecordKindMismatch {
                found: self.record_kind.clone(),
            });
        }

        let blocking_set: BTreeSet<&str> = self
            .release_blocking_family_refs
            .iter()
            .map(String::as_str)
            .collect();

        for row in &self.rows {
            self.validate_row(row, &blocking_set, &mut out);
        }

        self.validate_coverage(&mut out);
        self.validate_publication(&mut out);
        self.validate_summary(&mut out);

        out
    }

    fn validate_row(
        &self,
        row: &IngestionRow,
        blocking_set: &BTreeSet<&str>,
        out: &mut Vec<IngestionViolation>,
    ) {
        let id = row.entry_id.clone();

        for (field, value) in [
            ("entry_id", &row.entry_id),
            ("family_ref", &row.family_ref),
            ("surface_ref", &row.surface_ref),
            ("surface_summary", &row.surface_summary),
            ("canonical_source_ref", &row.canonical_source_ref),
            ("evidence_ref", &row.evidence_ref),
            ("rationale", &row.rationale),
        ] {
            if value.trim().is_empty() {
                out.push(IngestionViolation::EmptyField {
                    entry_id: id.clone(),
                    field: field.to_string(),
                });
            }
        }

        // The source packet must itself be coherent: effective <= ceiling.
        if row.canonical_published_label.rank() > row.canonical_claim_label.rank() {
            out.push(IngestionViolation::SourceLabelInconsistent {
                entry_id: id.clone(),
            });
        }

        // No widening: the published label cannot exceed the ceiling...
        if row.published_label.rank() > row.canonical_claim_label.rank() {
            out.push(IngestionViolation::PublishedExceedsCeiling {
                entry_id: id.clone(),
            });
        }
        // ...and must reflect the canonical effective label exactly (no stale copy).
        if row.published_label != row.canonical_published_label {
            out.push(IngestionViolation::PublishedNotCanonical {
                entry_id: id.clone(),
            });
        }

        // State / reason coherence.
        if row.ingest_state.is_current() {
            if !row.active_ingest_reasons.is_empty()
                || row.published_label != row.canonical_claim_label
            {
                out.push(IngestionViolation::CurrentRowNotBacked {
                    entry_id: id.clone(),
                });
            }
        } else if row.active_ingest_reasons.is_empty() {
            out.push(IngestionViolation::NarrowedRowWithoutReason {
                entry_id: id.clone(),
            });
        }

        // Below-cutline states must actually narrow below stable.
        if row.ingest_state.requires_below_cutline() && row.published_label.is_at_or_above_cutline()
        {
            out.push(IngestionViolation::NarrowedStateNotBelowCutline {
                entry_id: id.clone(),
            });
        }

        // Service-contract state is present iff this is a service-health row.
        match row.truth_surface {
            TruthSurface::ServiceHealth => {
                if row.service_contract_state.is_none() {
                    out.push(IngestionViolation::ServiceHealthMissingContractState {
                        entry_id: id.clone(),
                    });
                }
            }
            _ => {
                if row.service_contract_state.is_some() {
                    out.push(IngestionViolation::NonServiceHealthHasContractState {
                        entry_id: id.clone(),
                    });
                }
            }
        }

        // Release-blocking flag must agree with the declared family set.
        let declared_blocking = blocking_set.contains(row.family_ref.as_str());
        if declared_blocking != row.release_blocking {
            out.push(IngestionViolation::ReleaseBlockingMismatch { entry_id: id });
        }
    }

    fn validate_coverage(&self, out: &mut Vec<IngestionViolation>) {
        let covered_families: BTreeSet<FamilyKind> =
            self.rows.iter().map(|r| r.family_kind).collect();
        for kind in FamilyKind::ALL {
            if !covered_families.contains(&kind) {
                out.push(IngestionViolation::FamilyKindUncovered { family_kind: kind });
            }
        }

        let covered_surfaces: BTreeSet<TruthSurface> =
            self.rows.iter().map(|r| r.truth_surface).collect();
        for surface in TruthSurface::ALL {
            if !covered_surfaces.contains(&surface) {
                out.push(IngestionViolation::TruthSurfaceUncovered {
                    truth_surface: surface,
                });
            }
        }

        // Every covered family must appear on every truth surface.
        let pairs: BTreeSet<(FamilyKind, TruthSurface)> = self
            .rows
            .iter()
            .map(|r| (r.family_kind, r.truth_surface))
            .collect();
        for kind in &covered_families {
            for surface in TruthSurface::ALL {
                if !pairs.contains(&(*kind, surface)) {
                    out.push(IngestionViolation::FamilySurfaceCoverageGap {
                        family_kind: *kind,
                        truth_surface: surface,
                    });
                }
            }
        }
    }

    fn validate_publication(&self, out: &mut Vec<IngestionViolation>) {
        let expected = self.computed_decision();
        if self.publication.decision != expected {
            out.push(IngestionViolation::PublicationVerdictMismatch {
                found: self.publication.decision.clone(),
                expected,
            });
        }
    }

    /// The verdict implied by the rows: `hold` if any row widens or omits a
    /// required disclosure, `proceed` otherwise.
    fn computed_decision(&self) -> String {
        let any_widening = self.rows.iter().any(|r| {
            r.published_label != r.canonical_published_label
                || r.published_label.rank() > r.canonical_claim_label.rank()
        });
        if any_widening {
            "hold".to_string()
        } else {
            "proceed".to_string()
        }
    }

    fn validate_summary(&self, out: &mut Vec<IngestionViolation>) {
        let computed = self.computed_summary();
        let s = &self.summary;
        let checks: [(&str, bool); 17] = [
            ("total_rows", s.total_rows == computed.total_rows),
            (
                "total_families",
                s.total_families == computed.total_families,
            ),
            (
                "total_surfaces",
                s.total_surfaces == computed.total_surfaces,
            ),
            (
                "release_blocking_rows",
                s.release_blocking_rows == computed.release_blocking_rows,
            ),
            (
                "rows_below_cutline",
                s.rows_below_cutline == computed.rows_below_cutline,
            ),
            (
                "service_health_rows",
                s.service_health_rows == computed.service_health_rows,
            ),
            (
                "contradiction_rules_firing",
                s.contradiction_rules_firing == computed.contradiction_rules_firing,
            ),
            ("rows_current", s.rows_current == computed.rows_current),
            ("rows_stale", s.rows_stale == computed.rows_stale),
            ("rows_narrowed", s.rows_narrowed == computed.rows_narrowed),
            (
                "rows_policy_blocked",
                s.rows_policy_blocked == computed.rows_policy_blocked,
            ),
            (
                "rows_preview_only",
                s.rows_preview_only == computed.rows_preview_only,
            ),
            (
                "rows_underqualified",
                s.rows_underqualified == computed.rows_underqualified,
            ),
            (
                "help_about_rows",
                s.help_about_rows == computed.help_about_rows,
            ),
            (
                "release_center_rows",
                s.release_center_rows == computed.release_center_rows,
            ),
            (
                "support_export_rows",
                s.support_export_rows == computed.support_export_rows,
            ),
            (
                "public_truth_pack_rows",
                s.public_truth_pack_rows == computed.public_truth_pack_rows,
            ),
        ];
        for (field, ok) in checks {
            if !ok {
                out.push(IngestionViolation::SummaryMismatch {
                    field: field.to_string(),
                });
            }
        }
    }

    /// Recompute the roll-up counts directly from the rows.
    pub fn computed_summary(&self) -> IngestSummary {
        let count_state =
            |state: IngestState| self.rows.iter().filter(|r| r.ingest_state == state).count();
        let count_surface = |surface: TruthSurface| {
            self.rows
                .iter()
                .filter(|r| r.truth_surface == surface)
                .count()
        };
        let service_health_rows = count_surface(TruthSurface::ServiceHealth);
        IngestSummary {
            total_rows: self.rows.len(),
            total_families: self
                .rows
                .iter()
                .map(|r| r.family_kind)
                .collect::<BTreeSet<_>>()
                .len(),
            total_surfaces: self
                .rows
                .iter()
                .map(|r| r.truth_surface)
                .collect::<BTreeSet<_>>()
                .len(),
            release_blocking_rows: self.rows.iter().filter(|r| r.release_blocking).count(),
            rows_below_cutline: self
                .rows
                .iter()
                .filter(|r| !r.published_label.is_at_or_above_cutline())
                .count(),
            service_health_rows,
            contradiction_rules_firing: 0,
            rows_current: count_state(IngestState::Current),
            rows_stale: count_state(IngestState::Stale),
            rows_narrowed: count_state(IngestState::Narrowed),
            rows_policy_blocked: count_state(IngestState::PolicyBlocked),
            rows_preview_only: count_state(IngestState::PreviewOnly),
            rows_underqualified: count_state(IngestState::Underqualified),
            help_about_rows: count_surface(TruthSurface::HelpAbout),
            release_center_rows: count_surface(TruthSurface::ReleaseCenter),
            support_export_rows: count_surface(TruthSurface::SupportExport),
            public_truth_pack_rows: count_surface(TruthSurface::PublicTruthPack),
        }
    }

    /// All rows for one truth surface, in declared order.
    pub fn rows_for_surface(&self, surface: TruthSurface) -> impl Iterator<Item = &IngestionRow> {
        self.rows.iter().filter(move |r| r.truth_surface == surface)
    }

    /// All rows for one family, in declared order.
    pub fn rows_for_family(&self, family: FamilyKind) -> impl Iterator<Item = &IngestionRow> {
        self.rows.iter().filter(move |r| r.family_kind == family)
    }
}

