//! Typed truth-surface evidence-ingestion register that makes every M5 feature
//! family's canonical lifecycle state visible wherever users, admins, support
//! engineers, and release owners actually look.
//!
//! The M5 depth lanes ship behind a set of canonical, checked-in evidence
//! packets: the feature family register, the depth-claim qualification matrix,
//! the cross-surface hardening scorecards, and the certified evidence index.
//! Those packets are the single source of truth for *what each family currently
//! claims* and *how far each family is narrowed below stable*. The risk this
//! register removes is **drift**: a Help/About page, service-health row, release
//! center card, support export, or public-truth pack that once read "stable" and
//! keeps reading "stable" after the backing evidence aged out, narrowed, or hit a
//! policy block.
//!
//! This module is the **truth-surface ingestion register** for the M5 learnability
//! and boundary lane (the XT-12 learnability/boundary truth track). For every M5
//! feature family it records one row per user-/operator-facing truth surface
//! ([`TruthSurface`]). Each [`IngestionRow`]:
//!
//! - names the family it ingests ([`IngestionRow::family_kind`],
//!   [`IngestionRow::family_ref`]) and the canonical source-packet entry it reads
//!   ([`IngestionRow::canonical_source_ref`]);
//! - pins the canonical claim ceiling ([`IngestionRow::canonical_claim_label`])
//!   and the canonical effective label ([`IngestionRow::canonical_published_label`])
//!   straight from that source packet -- never retyped per surface;
//! - publishes a label ([`IngestionRow::published_label`]) that **must equal** the
//!   canonical effective label, so a surface can never advertise a wider claim than
//!   the packet backing it;
//! - exposes the surfacing state ([`IngestState`]) -- `current`, `stale`,
//!   `narrowed`, `policy_blocked`, `preview_only`, or `underqualified` -- so a
//!   reader can tell *why* a lane is below stable without reading internal notes;
//! - always discloses posture ([`PostureClass`]) -- `local_only`, `mirrored`,
//!   `managed`, or `browser_handoff` -- because boundary posture is never inferred;
//! - for service-health rows, carries the operational [`ServiceContractState`]
//!   (reused from the docs/help/About/service-health truth register) alongside the
//!   lifecycle label.
//!
//! The register is generated by `tools/regenerate_m5_truth_surface_ingestion.py`,
//! which reads the canonical feature family register and projects it onto the
//! truth surfaces, and is checked in at
//! `artifacts/release/m5/m5_truth_surface_evidence_ingestion.json` and embedded
//! here so this typed consumer and the CI gate agree on every row without a cargo
//! build in CI.
//!
//! The model is metadata-only: every field is a typed state or an opaque ref. It
//! carries no raw artifacts, logs, signatures, or credential material. Two classes
//! of check live outside this model because they need more than the register sees:
//! date arithmetic (recomputing freshness against an `as_of` date) and the
//! cross-artifact drift check (re-reading the canonical source packet to confirm
//! the ingested labels still match). Those live in the CI gate at
//! `tools/ci/m5/truth_surface_ingestion_check.py`. This model enforces the
//! structural and logical invariants that hold regardless of the clock and the
//! neighbouring artifact: the no-widening ceiling, state/label coherence,
//! posture disclosure, service-contract wiring, surface and family coverage,
//! and the publication verdict.

